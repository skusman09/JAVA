class TypeCasting {

    public static void main(String args[]) {
//     Widening Casting
        int a = 56;
        double b = a;
        System.out.println(a);
        System.out.println(b);

        
//         Narrowing Casting
        double x = 6767675;
        int y = (int)x;
        System.out.println(x);
        System.out.println(y);
    }
}
